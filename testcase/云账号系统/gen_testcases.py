def print_stats(label, wb):
    """输出单个工作簿的统计信息"""
    total = 0
    p0 = p1 = p2 = 0
    normal = abnormal = boundary = network = service = concurrent = compat = perf = security = upgrade_count = 0

    for sheet_name in wb.sheetnames:
        ws = wb[sheet_name]
        for row in range(2, ws.max_row + 1):
            total += 1
            priority = ws.cell(row=row, column=4).value
            if priority == 'P0': p0 += 1
            elif priority == 'P1': p1 += 1
            elif priority == 'P2': p2 += 1

            name = ws.cell(row=row, column=3).value or ''
            steps = ws.cell(row=row, column=8).value or ''
            stage = ws.cell(row=row, column=5).value or ''

            if '异常' in name or '错误' in name or '失败' in name:
                abnormal += 1
            elif '边界' in name or '格式' in name or '校验' in name:
                boundary += 1
            elif '弱网' in name or '断网' in name or '网络' in name:
                network += 1
            elif '并发' in name or '多用户' in name:
                concurrent += 1
            elif '兼容' in stage:
                compat += 1
            elif '性能' in stage:
                perf += 1
            elif '安全' in stage:
                security += 1
            elif '升级' in stage or '刷机' in name:
                upgrade_count += 1
            else:
                normal += 1

    print(f"\n{'='*50}")
    print(f"  {label}")
    print(f"{'='*50}")
    print(f"\n### 优先级分布\n")
    print(f"| 优先级 | 用例数量 | 占比 |")
    print(f"|--------|---------|------|")
    for lbl, cnt in [('P0', p0), ('P1', p1), ('P2', p2)]:
        pct = f"{cnt/total*100:.1f}%" if total else "0%"
        print(f"| {lbl} | {cnt} | {pct} |")
    print(f"| **总计** | **{total}** | **100%** |")

    print(f"\n### 场景覆盖\n")
    print(f"| 场景类型 | 用例数量 |")
    print(f"|---------|---------|")
    for lbl, cnt in [
        ('正常场景', normal), ('异常场景', abnormal), ('边界场景', boundary),
        ('网络异常场景', network), ('并发与多用户场景', concurrent),
        ('兼容性场景', compat), ('性能场景', perf), ('安全场景', security),
        ('升级/刷机场景', upgrade_count)
    ]:
        if cnt > 0:
            print(f"| {lbl} | {cnt} |")


