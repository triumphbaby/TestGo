VERSION = '1.8.0'
PERSON = 'kaixuan'
REQUIREMENT = 'AI学习任务'
MODULE_PREFIX = f'版本用例/{VERSION}/{PERSON}/{REQUIREMENT}'

HEADER_FONT = Font(bold=True, size=11)
HEADER_FILL = PatternFill(start_color='D9D9D9', end_color='D9D9D9', fill_type='solid')
CELL_ALIGNMENT = Alignment(wrap_text=True, vertical='top')
HEADER_ALIGNMENT = Alignment(wrap_text=True, vertical='center', horizontal='center')
THIN_BORDER = Border(
    left=Side(style='thin'), right=Side(style='thin'),
    top=Side(style='thin'), bottom=Side(style='thin'),
)

HEADERS = ['用例编号', '所属模块', '用例名称', '优先级', '前置条件', '步骤描述', '预期结果']
COL_WIDTHS = [16, 45, 42, 8, 55, 55, 55]
OUTPUT_DIR = os.path.dirname(os.path.abspath(__file__))


def mod(test_type, path):
    return f'{MODULE_PREFIX}/{test_type}/{path}'


def create_workbook():
    wb = Workbook()
    wb.remove(wb.active)
    return wb


def add_sheet(wb, sheet_name, rows):
    ws = wb.create_sheet(title=sheet_name)
    for col_idx, header in enumerate(HEADERS, 1):
        cell = ws.cell(row=1, column=col_idx, value=header)
        cell.font = HEADER_FONT
        cell.fill = HEADER_FILL
        cell.alignment = HEADER_ALIGNMENT
        cell.border = THIN_BORDER
    for row_idx, row_data in enumerate(rows, 2):
        for col_idx, value in enumerate(row_data, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=value)
            cell.alignment = CELL_ALIGNMENT
            cell.border = THIN_BORDER
    for col_idx, width in enumerate(COL_WIDTHS, 1):
        col_letter = chr(64 + col_idx) if col_idx <= 26 else 'A' + chr(64 + col_idx - 26)
        ws.column_dimensions[col_letter].width = width
    # 默认隐藏用例编号列（A列）
    ws.column_dimensions['A'].hidden = True


def save_workbook(wb, filename):
    filepath = os.path.join(OUTPUT_DIR, filename)
    wb.save(filepath)
    print(f'  -> {filepath}')


def save_merged_copy(wb, filename):
    """将多Sheet工作簿合并为单Sheet副本保存。"""
    merged = Workbook()
    ws = merged.active
    ws.title = '测试用例'
    # 写表头
    for col_idx, header in enumerate(HEADERS, 1):
        cell = ws.cell(row=1, column=col_idx, value=header)
        cell.font = HEADER_FONT
        cell.fill = HEADER_FILL
        cell.alignment = HEADER_ALIGNMENT
        cell.border = THIN_BORDER
    # 逐Sheet合并数据行
    current_row = 2
    for sheet_name in wb.sheetnames:
        src = wb[sheet_name]
        for row_idx in range(2, src.max_row + 1):
            for col_idx in range(1, len(HEADERS) + 1):
                value = src.cell(row=row_idx, column=col_idx).value
                cell = ws.cell(row=current_row, column=col_idx, value=value)
                cell.alignment = CELL_ALIGNMENT
                cell.border = THIN_BORDER
            current_row += 1
    for col_idx, width in enumerate(COL_WIDTHS, 1):
        col_letter = chr(64 + col_idx) if col_idx <= 26 else 'A' + chr(64 + col_idx - 26)
        ws.column_dimensions[col_letter].width = width
    # 默认隐藏用例编号列（A列）
    ws.column_dimensions['A'].hidden = True
    # 文件名加 _合并 后缀
    base, ext = os.path.splitext(filename)
    merged_path = os.path.join(OUTPUT_DIR, f'{base}_合并{ext}')
    merged.save(merged_path)
    print(f'  -> {merged_path} ({current_row - 2} rows)')


