all_sheets = [
    (sheet1_name, sheet1_cases),
    (sheet2_name, sheet2_cases),
    (sheet3_name, sheet3_cases),
    (sheet4_name, sheet4_cases),
    (sheet5_name, sheet5_cases),
]

wb = openpyxl.Workbook()
wb.remove(wb.active)  # 删除默认 sheet

for sheet_name, cases in all_sheets:
    ws = wb.create_sheet(title=sheet_name)

    # 写表头
    for col_idx, header in enumerate(HEADERS, 1):
        cell = ws.cell(row=1, column=col_idx, value=header)
        cell.font = HEADER_FONT
        cell.fill = HEADER_FILL
        cell.alignment = WRAP_ALIGN
        cell.border = THIN_BORDER

    # 写用例数据
    for row_idx, tc in enumerate(cases, 2):
        values = [
            tc["id"], tc["module"], tc["name"], tc["priority"],
            tc["phase"], tc["risk"], tc["pre"], tc["steps"], tc["expected"],
        ]
        for col_idx, val in enumerate(values, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=val)
            cell.font = CELL_FONT
            cell.alignment = WRAP_ALIGN
            cell.border = THIN_BORDER

    # 列宽
    for col_idx, width in enumerate(COL_WIDTHS, 1):
        ws.column_dimensions[get_column_letter(col_idx)].width = width

    # 隐藏 A 列（用例编号）
    ws.column_dimensions["A"].hidden = True

# 保存
output_dir = os.path.dirname(os.path.abspath(__file__))
output_path = os.path.join(output_dir, "DeviceInit_V190_pc_Functional.xlsx")
wb.save(output_path)

